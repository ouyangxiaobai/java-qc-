package javaBean;

public class PartPurchaseBean {

	private String partPurchaseId;
	private String partName;
	private String purTime;
	private String Amount;
	private String partPrice;
	private String staffId;
	private String supplierId;
	private String storageId;
	public String getPartPurchaseId() {
		return partPurchaseId;
	}
	public void setPartPurchaseId(String partPurchaseId) {
		this.partPurchaseId = partPurchaseId;
	}
	public String getPartName() {
		return partName;
	}
	public void setPartName(String partName) {
		this.partName = partName;
	}
	public String getPurTime() {
		return purTime;
	}
	public void setPurTime(String purTime) {
		this.purTime = purTime;
	}
	public String getAmount() {
		return Amount;
	}
	public void setAmount(String amount) {
		Amount = amount;
	}
	public String getPartPrice() {
		return partPrice;
	}
	public void setPartPrice(String partPrice) {
		this.partPrice = partPrice;
	}
	public String getStaffId() {
		return staffId;
	}
	public void setStaffId(String staffId) {
		this.staffId = staffId;
	}
	public String getSupplierId() {
		return supplierId;
	}
	public void setSupplierId(String supplierId) {
		this.supplierId = supplierId;
	}
	public String getStorageId() {
		return storageId;
	}
	public void setStorageId(String storageId) {
		this.storageId = storageId;
	}
	
	
}
